/*Um exemplo simples da estrutra while. 

Se colocar só o sinal de < menor sem o sinal de igual, vai contar apenas 5 vezes e não 6 vezes.*/
var c = 1;
while (c <= 6) {
  console.log(`Passo ${c}`);
  c++;
} /*c++ é o mesmo que colocar c = c +1
    c++ é uma forma útil de incremento, se ele era 1 vai passar a ser 2 e assim em diante. 
    
    Vc pode colocar para repetir quantas vezes que vc quiser, só mudando o número no comando dentro do while.*/
